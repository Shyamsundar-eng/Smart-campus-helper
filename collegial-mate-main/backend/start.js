#!/usr/bin/env node

import { spawn } from 'child_process';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

console.log('ðŸš€ Starting Collegial Mate Backend...');
console.log('ðŸ“ Working directory:', __dirname);

// Check if .env file exists
import { existsSync } from 'fs';
const envPath = join(__dirname, '.env');

if (!existsSync(envPath)) {
  console.log('âš ï¸  No .env file found. Please copy env.example to .env and configure your Google API credentials.');
  console.log('ðŸ“ See README.md for setup instructions.');
  process.exit(1);
}

// Start the server
const server = spawn('node', ['server.js'], {
  stdio: 'inherit',
  cwd: __dirname
});

server.on('error', (error) => {
  console.error('âŒ Failed to start server:', error);
  process.exit(1);
});

server.on('exit', (code) => {
  if (code !== 0) {
    console.error(`âŒ Server exited with code ${code}`);
    process.exit(code);
  }
});

// Handle process termination
process.on('SIGINT', () => {
  console.log('\nðŸ›‘ Shutting down server...');
  server.kill('SIGINT');
});

process.on('SIGTERM', () => {
  console.log('\nðŸ›‘ Shutting down server...');
  server.kill('SIGTERM');
});
